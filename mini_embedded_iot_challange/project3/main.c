#include "main.h"
#include "i2c_lcd.h"


ADC_HandleTypeDef hadc;
I2C_HandleTypeDef hi2c1;
TIM_HandleTypeDef htim2;

I2C_LCD_HandleTypeDef hlcd; /* LCD defined */

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC_Init(void);
static void MX_I2C1_Init(void);
static void MX_TIM2_Init(void);

uint32_t leftLDR;
uint32_t rightLDR;

void readLDR(){  /* Get LDR Values */

	HAL_ADC_Start(&hadc);
	
	HAL_ADC_PollForConversion(&hadc, HAL_MAX_DELAY);
	leftLDR = HAL_ADC_GetValue(&hadc);

	HAL_ADC_PollForConversion(&hadc, HAL_MAX_DELAY);
	rightLDR = HAL_ADC_GetValue(&hadc);
	
	HAL_ADC_Stop(&hadc);
}

uint16_t angle_to_ccr(uint8_t angle) {  /* Calculate CCR Value For Servo Motor */
	return 50 + (angle * (200 / 180));
}

int main(void)
{

  HAL_Init();
  SystemClock_Config();
	
  MX_GPIO_Init();
  MX_ADC_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();

  hlcd.hi2c = &hi2c1;
  hlcd.address = 0x4E;

  lcd_init(&hlcd);

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);

  while (1)
  {
  readLDR();

  if(leftLDR < 1000 && rightLDR > 1000){
	lcd_clear(&hlcd);
	lcd_puts(&hlcd,"Servo turned");
	lcd_gotoxy(&hlcd, 0, 1);
	lcd_puts(&hlcd, "to right !");
	__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, angle_to_ccr(0));
  }else if(leftLDR > 1000 && rightLDR < 1000){
	lcd_clear(&hlcd);
	lcd_puts(&hlcd,"Servo turned");
	lcd_gotoxy(&hlcd, 0, 1);
	lcd_puts(&hlcd, "to left !");
	__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, angle_to_ccr(180));
  }else{
        lcd_clear(&hlcd);
        lcd_puts(&hlcd,"Both LDR");
        lcd_gotoxy(&hlcd, 0, 1);
	lcd_puts(&hlcd, "brightened,too !");
         __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, angle_to_ccr(90));
  }
 }
}

